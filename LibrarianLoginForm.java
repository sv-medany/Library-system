    private void loginLibloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginLibloginActionPerformed
        String name = nameLibLogin.getText();
        int found = 0;
        String password = passwordLibLogin.getText();
        //take inputs from text fields to test them
        for (int i = 0; i < counterLibrarians; i++) {
            if (librarians[i].getName().equals(name) && librarians[i].getPassword().equals(password)) //test if the user name and password are correct
            {
                found = 1;
                LibrarianSection x = new LibrarianSection(); //if correct go to librarian section
                x.setVisible(true);
                this.hide();
            }
        }
        if (found == 0) //if incorrect
        {
            JOptionPane.showMessageDialog(this, "Wrong username or password.");
        }
    }//GEN-LAST:event_loginLibloginActionPerformed

