    public void readLibrarianFile() throws FileNotFoundException {
        File librariansFile = new File("Librarians.txt");
        Scanner s = new Scanner(librariansFile); //read file
        while (s.hasNextLine()) { //check file did not reach its end
            String x = s.nextLine();
            String toks[] = x.split(","); //fie is csv
            librarians[counterLibrarians] = new Librarian(toks[0], toks[1], toks[2], toks[3], toks[4], toks[5], toks[6]);//create new librarian using delimited fields
            counterLibrarians++;
        }
    }

