    private void adminLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminLoginActionPerformed
        String name = adminname.getText();
        int found = 0;
        String password = adminPassword.getText(); //get input from user to test

        for (int i = 0; i < counterAdmins; i++) {
            if (admins[i].Username.equals(name) && admins[i].Password.equals(password)) //check that user name and password are correct
            {
                found = 1;
                AdminSection x = new AdminSection(); //if correct then login
                x.setVisible(true);
                this.hide();
            }
        }
        if (found == 0) //if it nonexistent
        {
            JOptionPane.showMessageDialog(this, "Wrong username or password.");
        }

    }//GEN-LAST:event_adminLoginActionPerformed

