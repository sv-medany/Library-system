    public void readAdminFile() throws FileNotFoundException {
        File adminFile = new File("Admin.txt");
        Scanner s = new Scanner(adminFile); //read from the file
        while (s.hasNextLine()) { //checks file did not reach end
            String x = s.nextLine();
            String toks[] = x.split(","); //The file is csv
            admins[counterAdmins] = new Admin(toks[0], toks[1]); //creates new admin 
            counterAdmins++; //counts admin
        }
    }

