    public void readFileLibrariansDelete() throws FileNotFoundException {
        File librarianFile = new File("Librarians.txt");
        Scanner s = new Scanner(librarianFile); //read from file
        while (s.hasNextLine()) {//test file did ot reach its end
            String x = s.nextLine();
            String toks[] = x.split(","); //file is csv
            Librarian newL = new Librarian(toks[0], toks[1], toks[2], toks[3], toks[4], toks[5], toks[6]); //creates new librarian 
            Librarians.add(newL); //add to array list
        }
    }

    public void writeFileLibrariansDelete() throws FileNotFoundException {
        File flibrarian = new File("Librarian.txt");
        PrintWriter x = new PrintWriter(flibrarian);
        for (int i = 0; i < Librarians.size(); i++) {
            x.println(Librarians.get(i).getID() + "," + Librarians.get(i).getName() + "," + Librarians.get(i).getPassword() + "," + Librarians.get(i).getEmail() + "," + Librarians.get(i).getAddress() + "," + Librarians.get(i).getCity() + "," + Librarians.get(i).getContactNumber());
        }
        x.close();
    }

