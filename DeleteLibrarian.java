    private void deleteDeleteLibActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDeleteLibActionPerformed
        String id = idDelete.getText();
        //get fields to test for 
        boolean ErrorFlag = false, Found = false, EmptyFlag = false;
        if (id.isEmpty()) { //check if all fields are filled in
            EmptyFlag = true;
            JOptionPane.showMessageDialog(this, "one of the fields is empty, please try again");
        }
        if (!EmptyFlag) {
            if (id.charAt(0) == ' ') { //check all fields start with space
                JOptionPane.showMessageDialog(this, "The input must ot start with space, please try again");
                ErrorFlag = true;
            }
            for (int i = 0; i < id.length(); i++) {
                char c = id.charAt(i);
                if (!Character.isDigit(c)) { //check the name is all characters 
                    JOptionPane.showMessageDialog(this, "The name is invalid, please try again");
                    ErrorFlag = true;
                    break;
                }
            }

            if (!ErrorFlag) { //if no errors

                for (int i = 0; i < Librarians.size(); i++) {
                    if (Librarians.get(i).getID().equals(id)) { //check that the username and password exist
                        Librarians.remove(i); //delete from array list
                        Found = true;
                        JOptionPane.showMessageDialog(this, "Librarian deleted successfully");
                        try {
                            writeFileLibrariansDelete();
                        } catch (FileNotFoundException ex) {
                            Logger.getLogger(DeleteLibrarian.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        AdminSection xy = new AdminSection();
                        xy.setVisible(true);
                        this.hide();
                        break;
                    }
                }
                if (!Found) { //librarian not found
                    JOptionPane.showMessageDialog(this, "The Librarian is not found, please try again");
                }
            }
        }


    }//GEN-LAST:event_deleteDeleteLibActionPerformed

